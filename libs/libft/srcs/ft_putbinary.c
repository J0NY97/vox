#include "unistd.h"

void	ft_putbinary(int num, int n)
{
    int	d;
	int	t;
    
    t = 0;
    while (--n >= 0)
    {
        d = num >> n;
        if (d & 1)
            write(1, "1", 1);
        else
            write(1, "0", 1);
        t++;
    }
}